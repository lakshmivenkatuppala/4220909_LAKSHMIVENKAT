def climbingLeaderboard(ranked, player):
    # Write your code here
    unq=sorted(list(set(ranked)), reverse=True)
    n=len(unq)
    m=n-1
    ranks=[]
    
    for score in player:
        while m>=0 and score>=unq[m]:
            m-=1
        ranks.append(m+2)
    return ranks


